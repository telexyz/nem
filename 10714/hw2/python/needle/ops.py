class Summation(TensorOp):
    def __init__(self, axes: Optional[tuple] = None):
        self.axes = axes

    def compute(self, a):
        ### BEGIN YOUR SOLUTION
        return array_api.sum(a, self.axes)
        ### END YOUR SOLUTION

    def gradient(self, out_grad, node):
        ### BEGIN YOUR SOLUTION
        a = node.inputs[0]
        s = out_grad.shape
        axes = self.axes
        if axes is None: axes = ()
        if not isinstance(axes, tuple): axes = (axes,)
        for i in range(len(axes)):
            idx = axes[i]
            s = s[0:idx] + (1,) + s[idx:]
        r = reshape(out_grad, s)
        return (broadcast_to(r, a.shape), )
        ### END YOUR SOLUTION


def summation(a, axes=None):
    return Summation(axes)(a)


class MatMul(TensorOp):
    def compute(self, a, b):
        ### BEGIN YOUR SOLUTION
        return array_api.matmul(a, b)
        ### END YOUR SOLUTION

    def gradient(self, out_grad, node):
        ### BEGIN YOUR SOLUTION
        a, b = node.inputs 

        bt = Transpose()(b)
        l = MatMul()(out_grad, bt)

        n = len(l.shape)-len(a.shape)
        if n > 0:
            axes = ()
            for i in range(n): axes += (i,)
            l = summation(l, axes=axes)


        at = Transpose()(a)
        r = MatMul()(at, out_grad)

        n = len(r.shape)-len(b.shape)
        if n > 0:
            axes = ()
            for i in range(n): axes += (i,)
            r = summation(r, axes=axes)

        return (l, r)
        ### END YOUR SOLUTION


